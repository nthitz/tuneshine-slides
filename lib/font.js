export function textWidth(text, scale = 1) {
  return Array.from(text.toUpperCase()).reduce((width, character, index, chars) => {
    const glyph = font[character] ?? font["?"];
    const glyphWidth = Math.max(...glyph.map((row) => row.length));
    const gap = index === chars.length - 1 ? 0 : 1;
    return width + (glyphWidth + gap) * scale;
  }, 0);
}

export function drawText(canvas, text, x, y, color, scale = 1) {
  let cursorX = x;
  for (const character of text.toUpperCase()) {
    const glyph = font[character] ?? font["?"];
    const glyphWidth = Math.max(...glyph.map((row) => row.length));

    for (let row = 0; row < glyph.length; row += 1) {
      for (let column = 0; column < glyph[row].length; column += 1) {
        if (glyph[row][column] === "1") {
          canvas.rect(cursorX + column * scale, y + row * scale, scale, scale, color);
        }
      }
    }

    cursorX += (glyphWidth + 1) * scale;
  }
}

export function drawCenteredText(canvas, text, y, color, scale = 1) {
  drawText(canvas, text, Math.floor((canvas.width - textWidth(text, scale)) / 2), y, color, scale);
}
